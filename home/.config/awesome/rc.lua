function volume_up()
    awful.util.spawn("amixer -q set Master 10%+")
    vicious.force({volbar, volstate})
end

function volume_down()
    awful.util.spawn("amixer -q set Master 10%-")
    vicious.force({volbar, volstate})
end

function volume_mute()
    awful.util.spawn("amixer -q set Master toggle")
    vicious.force({volbar, volstate})
end

